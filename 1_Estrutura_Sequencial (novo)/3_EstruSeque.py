# Faça um Programa que peça dois números e imprima a soma.

primeiroNumeroText = str("Informe primeiro número: ")
segundoNumeroText = str("Informe segundo número: ")

num1 = float(input(primeiroNumeroText))
num2 = float(input(segundoNumeroText))

soma = num1 + num2

print("A soma de ", num1, " + ", num2, " = ", soma)


